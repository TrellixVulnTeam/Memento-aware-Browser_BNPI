import 'chrome://resources/cr_elements/cr_button/cr_button.m.js';
import 'chrome://resources/cr_elements/cr_icon_button/cr_icon_button.m.js';
import './shared_css.js';

import {assert} from 'chrome://resources/js/assert.m.js';
import {WebUIListenerBehavior} from 'chrome://resources/js/web_ui_listener_behavior.m.js';
import {html, Polymer} from 'chrome://resources/polymer/v3_0/polymer/polymer_bundled.min.js';

import {AutogeneratedThemeColorInfo} from '../manage_profiles_browser_proxy.js';
import {navigateToPreviousRoute, navigateToStep, ProfileCreationSteps, Routes} from '../navigation_behavior.js';

Polymer({
  is: 'profile-type-choice',

  _template: html`<!--_html_template_start_-->
<style include="profile-creation-shared">
  #actionContainer {
    align-items: center;
    display: flex;
    flex-direction: column;
  }

  #signinPromoBanner {
    background-image: url(profile_creation_flow/images/banner_light_image.svg);
  }

  @media (prefers-color-scheme: dark) {
    #signinPromoBanner {
      background-image:
          url(profile_creation_flow/images/banner_dark_image.svg);
    }
  }

  #bannerContainer {
    height: 266px;
    position: relative;
    width: 100%;
  }

  #notNowButton {
    background: none;
    border: none;
    color: var(--cr-link-color);
    cursor: pointer;
    font-size: 1em;
    font-weight: normal;
    line-height: 20px;
    margin-top: 18px;
    outline: none;
  }

  #signInButton {
    font-size: 1em;
    font-weight: normal;
    width: 208px;
  }

  .title-container {
    margin: 104px auto;
    margin-bottom: 48px;
    text-align: center;
  }
</style>

<div id="headerContainer"
    style$="--theme-frame-color:[[profileThemeInfo.themeFrameColor]];
            --theme-text-color:[[profileThemeInfo.themeFrameTextColor]];">
  <cr-icon-button id="backButton" class="icon-arrow-back"
      on-click="onClickBack_" aria-label="$i18n{backButtonLabel}"
      disabled="[[loadSigninInProgess_]]">
  </cr-icon-button>
  <div id="signinPromoBanner" class="banner"></div>
  <img class="avatar" src="[[profileThemeInfo.themeGenericAvatar]]">
</div>
<div class="title-container">
  <h2>$i18n{profileTypeChoiceTitle}</h2>
  <h3>$i18n{profileTypeChoiceSubtitle}</h3>
</div>
<div id="actionContainer">
  <cr-button id="signInButton" class="action-button fade-in"
      on-click="onSignInClick_" disabled="[[loadSigninInProgess_]]">
    $i18n{signInButtonLabel}
  </cr-button>
  <button id="notNowButton" class="action-link fade-in"
      on-click="onNotNowClick_" disabled="[[loadSigninInProgess_]]">
    $i18n{notNowButtonLabel}
  </button>
</div>
<!--_html_template_end_-->`,

  behaviors: [WebUIListenerBehavior],

  properties: {
    /** @type {!AutogeneratedThemeColorInfo} */
    profileThemeInfo: {
      type: Object,
    },

    /**
     * If true, all buttons are disabled (to avoid creating two profiles).
     * @private {boolean}
     */
    loadSigninInProgess_: {
      type: Boolean,
      value: false,
    },
  },

  /** @override */
  ready() {
    this.addWebUIListener(
        'load-signin-failed', () => this.handleLoadSigninFailed_());
  },

  /** @private */
  onNotNowClick_() {
    navigateToStep(
        Routes.NEW_PROFILE, ProfileCreationSteps.LOCAL_PROFILE_CUSTOMIZATION);
  },

  /** @private */
  onSignInClick_() {
    assert(!this.loadSigninInProgess_);
    this.loadSigninInProgess_ = true;
    navigateToStep(Routes.NEW_PROFILE, ProfileCreationSteps.LOAD_SIGNIN);
  },

  /** @private */
  onClickBack_() {
    navigateToPreviousRoute();
  },

  /** @private */
  handleLoadSigninFailed_() {
    // TODO(crbug.com/1126913): Show some error message to inform the user.
    this.loadSigninInProgess_ = false;
  }
});
